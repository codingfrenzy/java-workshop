public abstract class ASolution {
    /*
    ONE LINE PRINTS
     */
    void println(String s){
        System.out.println(s);
    }

    void println(char s){
        System.out.println(s);
    }

    void println(float s){
        System.out.println(s);
    }

    void println(int s){
        System.out.println(s);
    }

    void println(boolean s){
        System.out.println(s);
    }

    /*
    SAME LINE PRINTS
     */
    void print(String s){
        System.out.println(s);
    }

    void print(char s){
        System.out.println(s);
    }

    void print(float s){
        System.out.println(s);
    }

    void print(int s){
        System.out.println(s);
    }

    void print(boolean s){
        System.out.println(s);
    }

    abstract void runSolution();
}
